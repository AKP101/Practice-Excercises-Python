class calculator():
    
    def add(num1, num2):
        added = num1 +num2
        return (added)
    def subtract(x,y):
        sub = x-y
        return sub
    def multiplication(x,y):
        mult = x*y
        return mult
    def division(x,y):
        div = x/y
        return div


